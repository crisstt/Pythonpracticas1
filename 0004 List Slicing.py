list1 = [0,10,20,30,50,100,200,8,25,35,42,45,55]

print("Imprimir la lista de dos en dos" )
print(list1[::2])
print("\n")
print("Imprimir la lista de tres en tres sin contar el primero ni el ultimo" )
print(list1[1:12:3])
print("\n")
print("Imprimir la lista de regreso" )
print(list1[::-1])
print("\n")
print("Imprimir la lista invertida de 2 en 2" )
print(list1[-1:-14:-2])
print("\n")

